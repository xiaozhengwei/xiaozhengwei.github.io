package com.utunan.ioc.java.beans;

/**
 * 描述人的Pojo
 *
 * Setter Getter 方法
 *  
 *
 */
public class Person {
    String name;
    String age;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }
}
